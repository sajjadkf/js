// مباحث مربوط به ارایه

var number = [ 2 , 4 , 11 , 13]
console.log(number)

// push اضافه میکنه به ارایه و مقدار میخواد
number.push(20)
console.log(number)

//pop حذف میکنه مقدار نمیخواد
number.pop()
console.log(number)

//unshift اضافه میکنه از اول و عدد میخواد
number.unshift(12,10)
console.log(number)

//shift حذف میکنه از اول 
number.shift()
number.shift()
console.log(number)

